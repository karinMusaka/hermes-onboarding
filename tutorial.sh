#!/bin/bash
# ============================================================================
# Hermes チュートリアル — ハンズオン形式
# ============================================================================
# 実際に hermes コマンドを叩きながら学ぶ。
# wizard.sh から自動で呼ばれる。単独実行も可能。
# ============================================================================

set -e

# ── 色定義 ──
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
RED='\033[0;31m'
BOLD='\033[1m'
DIM='\033[2m'
NC='\033[0m'

ok()    { echo -e "${GREEN}✅${NC} $1"; }
hint()  { echo -e "  ${CYAN}💡${NC} $1"; }
pause() { echo ""; echo -e "${DIM}  Enterで次へ...${NC}"; read -r; }

# PATH 確保
export PATH="$HOME/.local/bin:$PATH"

# hermes が使えるか確認
if ! command -v hermes &>/dev/null; then
    echo -e "${RED}❌ hermes コマンドが見つかりません${NC}"
    echo "  先に install.sh を実行してください"
    exit 1
fi

PROGRESS_FILE="$HOME/.hermes/onboarding/progress.txt"
mkdir -p "$(dirname "$PROGRESS_FILE")"
touch "$PROGRESS_FILE"

mark_done() {
    echo "$1" >> "$PROGRESS_FILE"
}

is_done() {
    grep -q "^$1$" "$PROGRESS_FILE" 2>/dev/null
}

# ============================================================================
clear
cat << 'BANNER'

  ╔══════════════════════════════════════════╗
  ║                                          ║
  ║   📖 Hermes チュートリアル               ║
  ║   基本操作を一緒にやってみましょう        ║
  ║                                          ║
  ║   全5レッスン（約10分）                   ║
  ║   途中でやめても、続きからできます        ║
  ║                                          ║
  ╚══════════════════════════════════════════╝

BANNER
pause

# ============================================================================
# レッスン 1: 挨拶
# ============================================================================
if ! is_done "lesson1"; then
clear
cat << 'L1'

  ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
    レッスン 1/5: Hermes と話してみよう
  ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  Hermes はターミナルで動く AI アシスタントです。
  hermes "質問" と入力すると、AI が答えてくれます。

L1

echo -e "  ${BOLD}やってみよう:${NC}"
echo ""
echo -e "  以下のコマンドをコピペして Enter を押してください:"
echo ""
echo -e "    ${CYAN}hermes \"こんにちは、自己紹介してください\"${NC}"
echo ""
echo -e "${DIM}  ─── ここから Hermes の回答 ───${NC}"
echo ""

timeout 30 hermes "こんにちは、簡潔に自己紹介してください。2文以内で。" 2>/dev/null || {
    echo -e "${YELLOW}  (Hermes の起動に少し時間がかかることがあります)${NC}"
}

echo ""
echo -e "${DIM}  ─── ここまで ───${NC}"
echo ""
ok "できました！Hermes が返事してくれましたね。"
echo ""
echo -e "  ${BOLD}📝 覚えておこう:${NC}"
echo "    hermes \"質問\"  → 1回だけ質問（ワンショット）"
echo "    hermes         → チャットモード（会話を続けられる）"
echo "    Ctrl+C         → 中断・終了"

mark_done "lesson1"
pause
fi

# ============================================================================
# レッスン 2: ファイル操作
# ============================================================================
if ! is_done "lesson2"; then
clear
cat << 'L2'

  ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
    レッスン 2/5: ファイルを作ってみよう
  ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  Hermes はあなたの Mac のファイルを作ったり読んだりできます。

L2

echo -e "  ${BOLD}やってみよう:${NC}"
echo ""
echo -e "  Hermes にファイルを作ってもらいます..."
echo ""

timeout 30 hermes "デスクトップに hermes_test.txt というファイルを作ってください。中身は「Hermes チュートリアル完了！」と書いてください。作成したら完了と教えてください。" 2>/dev/null || true

echo ""

if [[ -f "$HOME/Desktop/hermes_test.txt" ]]; then
    ok "デスクトップに hermes_test.txt ができました！"
    echo ""
    echo -e "  ${DIM}中身: $(cat "$HOME/Desktop/hermes_test.txt")${NC}"
    echo ""
    hint "Finder でデスクトップを見てみてください。ファイルがあるはずです！"
else
    echo -e "  ${YELLOW}ファイルが見つかりませんでした。大丈夫、あとで試せます。${NC}"
fi

echo ""
echo -e "  ${BOLD}📝 覚えておこう:${NC}"
echo "    Hermes は自然な日本語で指示するだけでファイルを操作できます。"
echo "    「〇〇を作って」「〇〇を読んで」「〇〇を修正して」等"

mark_done "lesson2"
pause
fi

# ============================================================================
# レッスン 3: 情報収集
# ============================================================================
if ! is_done "lesson3"; then
clear
cat << 'L3'

  ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
    レッスン 3/5: 調べものをしてみよう
  ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  Hermes はインターネットの情報も調べられます。
  会議の準備やリサーチに便利です。

L3

echo -e "  ${BOLD}やってみよう:${NC}"
echo ""
echo -e "  今日の日付と曜日を聞いてみましょう..."
echo ""

timeout 30 hermes "今日は何年何月何日の何曜日ですか？簡潔に答えてください。" 2>/dev/null || true

echo ""
ok "Hermes は日付や時間も把握しています。"
echo ""
echo -e "  ${BOLD}📝 こんな使い方もできます:${NC}"
echo "    「〇〇について3つのポイントでまとめて」"
echo "    「△△と□□の違いを表で比較して」"
echo "    「今週のミーティングの議題案を作って」"

mark_done "lesson3"
pause
fi

# ============================================================================
# レッスン 4: ビジネス活用
# ============================================================================
if ! is_done "lesson4"; then
clear
cat << 'L4'

  ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
    レッスン 4/5: ビジネスで活用してみよう
  ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  Hermes はビジネス文書の作成も得意です。

L4

echo -e "  ${BOLD}やってみよう:${NC}"
echo ""
echo -e "  メールの下書きを作ってもらいましょう..."
echo ""

timeout 30 hermes "取引先への打ち合わせ日程調整メールの下書きを作ってください。来週の火曜か水曜の午後を候補にして。短く丁寧に。" 2>/dev/null || true

echo ""
ok "メールの下書きができました！"
echo ""
echo -e "  ${BOLD}📝 こんな使い方もできます:${NC}"
echo "    「この文章をもっとフォーマルにして」"
echo "    「英語に翻訳して」"
echo "    「プレゼンのアウトラインを作って」"
echo "    「この数字のグラフ用にCSVを作って」"

mark_done "lesson4"
pause
fi

# ============================================================================
# レッスン 5: 便利機能
# ============================================================================
if ! is_done "lesson5"; then
clear
cat << 'L5'

  ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
    レッスン 5/5: 覚えておくと便利なこと
  ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  最後に、知っておくと便利なことをまとめます。

L5

echo ""
echo -e "  ${BOLD}🔧 基本コマンド:${NC}"
echo ""
echo "    hermes              チャット開始"
echo "    hermes \"質問\"       1回だけ質問"
echo "    hermes status       状態を確認"
echo "    hermes setup        設定を変更"
echo "    Ctrl+C              中断・終了"
echo ""
echo -e "  ${BOLD}💡 コツ:${NC}"
echo ""
echo "    ・具体的に指示するほど良い結果になります"
echo "      ✗「メール書いて」"
echo "      ○「取引先の田中さんに来週の会議のリスケをお願いするメールを書いて」"
echo ""
echo "    ・Hermes は会話の流れを覚えています"
echo "      「さっきの文章をもう少し短くして」が通じます"
echo ""
echo "    ・困ったら「使い方を教えて」と聞けばOK"
echo ""
echo -e "  ${BOLD}🆘 困ったとき:${NC}"
echo ""
echo "    ・画面が固まった → Ctrl+C で中断"
echo "    ・エラーが出た   → Hermes にエラーを貼り付けて聞く"
echo "    ・わからない     → 木原さんに聞く or Hermes に聞く"
echo ""

mark_done "lesson5"
fi

# ============================================================================
# 完了
# ============================================================================
echo ""
cat << 'COMPLETE'

  ╔══════════════════════════════════════════╗
  ║                                          ║
  ║   🎓 チュートリアル完了！                 ║
  ║                                          ║
  ║   お疲れさまでした！                      ║
  ║   これで Hermes の基本はバッチリです。     ║
  ║                                          ║
  ║   hermes と入力してチャットを始めよう！    ║
  ║                                          ║
  ╚══════════════════════════════════════════╝

COMPLETE

echo -e "  ${BOLD}🎯 次のステップ: ミッション${NC}"
echo ""
echo "  練習問題が用意されています。"
echo "  日常業務で使いながらレベルアップしましょう！"
echo ""
echo "    bash ~/.hermes/onboarding/missions.sh     ← ミッション一覧を見る"
echo ""
echo "  もう一度チュートリアルをやりたいとき:"
echo "    bash ~/.hermes/onboarding/tutorial.sh"
echo ""
